@login_required(login_url='signin')
def index(request):
    user_object = User.objects.get(username=request.user.username)
    user_profile = Profile.objects.get(user=user_object)

    user_following_list = []
    feed = []
    if(not request.user.is_authenticated):
        return redirect('signin')
    user_following = FollowersCount.objects.filter(follower=request.user.username)
    # aggiorno il numero di followers
    for users in user_following:
        user_following_list.append(users.user)

    for usernames in user_following_list:
        feed_lists = Post.objects.filter(user=usernames)
        feed.append(feed_lists)
    
    feed_list = list(chain(*feed))

    # utenti suggeriti
    all_users = User.objects.all()
    user_following_all = []

    for user in user_following:
        user_list = User.objects.get(username=user.user)
        user_following_all.append(user_list)
    
    new_suggestions_list = [x for x in list(all_users) if (x not in list(user_following_all))]
    current_user = User.objects.filter(username=request.user.username)
    final_suggestions_list = [x for x in list(new_suggestions_list) if ( x not in list(current_user))]
    random.shuffle(final_suggestions_list)

    username_profile = []
    username_profile_list = []

    for users in final_suggestions_list:
        username_profile.append(users.id)

    for ids in username_profile:
        profile_lists = Profile.objects.filter(id_user=ids)
        username_profile_list.append(profile_lists)

    suggestions_username_profile_list = list(chain(*username_profile_list))

    #recent posts suggested:
    recent_posts = Post.objects.order_by('-created_at')[:7]


    context = {'user_profile': user_profile, 'posts':feed_list, 'suggestions_username_profile_list': suggestions_username_profile_list[:10], 'recent_posts': recent_posts}
    return render(request, 'index.html',context)


@login_required(login_url='signin')
def like_post(request):
    username = request.user.username
    post_id = request.POST.get('post_id')

    post = Post.objects.get(id=post_id)

    like_filter = LikePost.objects.filter(post_id=post_id, username=username).first()

    if like_filter is None:
        new_like = LikePost.objects.create(post_id=post_id, username=username)
        new_like.save()
        post.no_of_likes += 1
        liked = True
    else:
        like_filter.delete()
        post.no_of_likes -= 1
        liked = False

    post.save()

    response_data = {
        'likes_count': post.no_of_likes,
        'liked': liked,
    }

    return JsonResponse(response_data)

